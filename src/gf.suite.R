checkIfGapfillIsPossible <- function(met) {
  # metabolite in [e0] compartment ?
  do.gf <- any(mseed[id %in% core.plus.present.rxns, grepl(gsub("\\[e0","\\[1",met),equation, fixed = T)])
  return(do.gf)
}

